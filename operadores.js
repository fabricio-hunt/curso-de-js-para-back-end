/*Os operadores em JavaScript são ferramentas essenciais para realizar diversas operações e manipulações de dados na linguagem. Eles permitem que você execute cálculos, compare valores, combine strings, avalie condições lógicas e muito mais. Dominá-los é crucial para escrever código JavaScript eficiente, expressivo e robusto.

Tipos de Operadores em JavaScript:

Existem diversos tipos de operadores em JavaScript, cada um com sua função específica. Os principais tipos são:

Operadores Aritméticos: Realizam operações matemáticas básicas, como adição, subtração, multiplicação, divisão e expoenciação. Exemplos: +, -, *, /, ^.
Operadores de Atribuição: Atribuem valores a variáveis. O operador básico é =, mas existem outros como +=, -=, *=, /=.
Operadores de Comparação: Comparação de valores, resultando em valores booleanos (verdadeiro ou falso). Exemplos: ==, !=, >, <, >=, <=.
Operadores Lógicos: Combinam valores booleanos para realizar operações lógicas complexas. Exemplos: && (e), || (ou), ! (não).
Operadores de String: Manipulam strings de texto. Exemplos: + (concatenação), [] (acesso a caracteres), .length (tamanho da string).
Operadores Bitwise: Operações bit a bit em valores numéricos. Exemplos: & (and bitwise), | (or bitwise), ~ (not bitwise), << (shift left), >> (shift right).
Operador Ternário: Um operador conciso para expressar instruções condicionais. Sintaxe: condição ? valor1 : valor2.
Operador Vírgula: Separa expressões em uma instrução.*/


//Exemplos de Uso dos Principais Operadores em JavaScript
//Operadores Aritméticos:
/*
Adição (+): Combina dois valores numéricos.
Exemplo: var soma = 10 + 5; // soma = 15
Subtração (-): Subtrai um valor numérico de outro.
Exemplo: var diferenca = 20 - 7; // diferenca = 13
Multiplicação (*): Multiplica dois valores numéricos.
Exemplo: var produto = 3 * 6; // produto = 18
Divisão (/): Divide um valor numérico por outro.
Exemplo: var quociente = 49 / 7; // quociente = 7
Expoenciação (^): Eleva um valor numérico a uma potência.
Exemplo: var potencia = 2 ^ 3; // potencia = 8
Operadores de Atribuição:

Atribuição Simples (=): Atribui um valor a uma variável.
Exemplo: var numero = 12;
Atribuição com Adição (+=): Soma e atribui o resultado a uma variável.
Exemplo: var contador = 0; contador += 5; // contador = 5
Atribuição com Subtração (-=): Subtrai e atribui o resultado a uma variável.
Exemplo: var valor = 10; valor -= 3; // valor = 7
Atribuição com Multiplicação (*=): Multiplica e atribui o resultado a uma variável.
Exemplo: var total = 2; total *= 7; // total = 14
Atribuição com Divisão (/=): Divide e atribui o resultado a uma variável.
Exemplo: var quociente = 16; quociente /= 4; // quociente = 4
Operadores de Comparação:

Igualdade (==): Verifica se dois valores são iguais.
Exemplo: var x = 10; var y = 10; var resultado = x == y; // resultado = true
Desigualdade (!=): Verifica se dois valores são diferentes.
Exemplo: var a = 5; var b = 7; var resposta = a != b; // resposta = true
Maior que (>): Verifica se um valor é maior que outro.
Exemplo: var c = 20; var d = 15; var comparacao = c > d; // comparacao = true
Menor que (<): Verifica se um valor é menor que outro.
Exemplo: var e = 3; var f = 8; var condicao = e < f; // condicao = true
Maior ou igual a (>=): Verifica se um valor é maior ou igual a outro.
Exemplo: var g = 12; var h = 12; var comparacao2 = g >= h; // comparacao2 = true
Menor ou igual a (<=): Verifica se um valor é menor ou igual a outro.
Exemplo: var i = 16; var j = 10; var condicao2 = i <= j; // condicao2 = false
Operadores Lógicos:

E (&&): Verifica se duas condições são verdadeiras ao mesmo tempo.
Exemplo: var tempoBom = true; var estouFeliz = true; var possoSair = tempoBom && estouFeliz; // possoSair = true
Ou (||): Verifica se pelo menos uma das duas condições é verdadeira.
Exemplo: var estaFome = true; var tenhoDinheiro = false; var possoComer = estaFome || tenhoDinheiro; // possoComer = true
Negação (!): Inverte o valor booleano de uma condição.
Exemplo: var estouDoente = false; var estouComSaude = !estouDoente; // estouComSaude = true*/